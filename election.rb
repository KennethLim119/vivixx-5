$votecounter = 0
$candidate = [
	{ candidate: "John Dorian", position: "President", votecount: 0},
	{ candidate: "Christopher Turk", position: "President", votecount: 0},
	{ candidate: "Shawn Spencer", position: "President", votecount: 0},
	{ candidate: "Burton Guster", position: "President", votecount: 0}
]

def getVote
	puts "Please select the full name of your preferred President"
	$candidate.each do |cand|
		puts "#{cand[:candidate]} for #{cand[:position]} Votes: #{cand[:votecount]}"
	end
	vote = gets.chomp
	$votecounter += 1

	if vote == "John Dorian"
		$candidate[vote.to_i][:votecount] += 1
	elsif vote == "Christopher Turk"
		$candidate[vote.to_i][:votecount] += 1
	elsif vote == "Shawn Spencer"
		$candidate[vote.to_i][:votecount] += 1
	elsif vote == "Burton Guster"
		$candidate[vote.to_i][:votecount] += 1
	elsif vote == "E"
		puts "Nothing to see here!"
		exit
	elsif vote != [:candidate]
		puts "There is no candidate with that name"
		execute
	end
end

def showVotes
	if $votecounter == 24
		puts "Already reached the maximum amount of votes"
		$candidate.each do |cand|
		puts "#{cand[:candidate]} for #{cand[:position]} Votes: #{cand[:votecount]}"
		end
	else
		execute
	end
end

def execute
	getVote
	showVotes
end

